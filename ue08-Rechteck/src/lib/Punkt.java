package lib;

class Punkt {
    private double x; // x-Koordinate des Punktes
    private double y; // y-Koordinate des Punktes

    public Punkt(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
